#!/usr/bin/env python3
"""
type-annotated function add that takes a float a and a 
float b as arguments and returns their sum as a float. 
"""

def add(a: float, b: float) -> float:
    """
       Args:
           a: The first parameter
           b: The second parameter

       
       Return:
           Sum of two floats


    """

    return (a + b)
